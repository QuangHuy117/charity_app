
class Task {
  String name;
  String location;
  String startDate;
  String endDate;
  int numberMember;
  String description;
  String underTaken;
  bool status;

  Task({
    required this.name,
    required this.location,
    required this.startDate,
    required this.endDate,
    required this.numberMember,
    required this.description,
    required this.underTaken,
    required this.status,
  });
}